// ─── Utilitaires de recherche ───────────────────────────────────────────────

export function searchVerres(query: string, filters?: {
  marque?: string;
  type?: VerreType;
  indice?: number;
  classeSS?: 1 | 2;
}): Verre[] {
  const q = query.toLowerCase().trim();
  return VERRES_DB.filter((v) => {
    if (filters?.marque && v.marque !== filters.marque) return false;
    if (filters?.type && v.type !== filters.type) return false;
    if (filters?.indice && v.indice !== filters.indice) return false;
    if (filters?.classeSS && v.classeSS !== filters.classeSS) return false;
    if (!q) return true;
    return (
      v.designation.toLowerCase().includes(q) ||
      v.marque.toLowerCase().includes(q) ||
      v.gamme.toLowerCase().includes(q) ||
      (v.tags ?? []).some((t) => t.toLowerCase().includes(q))
    );
  });
}

export function getVerresByMarque(marque: string): Verre[] {
  return VERRES_DB.filter((v) => v.marque === marque);
}

export function getVerresProgressifs(): Verre[] {
  return VERRES_DB.filter((v) => v.type === "progressif");
}

export function getVerresUnifo(): Verre[] {
  return VERRES_DB.filter((v) => v.type === "unifocal");
}

export function getVerresByClasse(classe: 1 | 2): Verre[] {
  return VERRES_DB.filter((v) => v.classeSS === classe);
}
