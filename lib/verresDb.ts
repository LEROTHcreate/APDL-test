export type VerreType = "unifocal" | "bifocal" | "progressif" | "degressif";
export type VerreMatiere = "organique" | "polycarbonate" | "trivex" | "mineral";
export type VerreUsage = "unifocal-vl" | "unifocal-vp" | "progressif-confort" | "progressif-standard" | "progressif-premium" | "bifocal" | "lentille";

export interface Verre {
  id: string;
  marque: string;
  gamme: string;
  designation: string;
  /** Description commerciale complète — traitements, avantages, arguments de vente */
  description?: string;
  type: VerreType;
  matiere: VerreMatiere;
  /** Index de réfraction ex: 1.5 | 1.6 | 1.67 | 1.74 */
  indice: number;
  /** Traitements inclus */
  traitements: string[];
  /** Code LPPR SS (peut être vide si hors nomenclature) */
  lppr?: string;
  /** Classe SS : 1 = remboursé nomenclature / 2 = remboursé 100% santé */
  classeSS?: 1 | 2;
  /** Prix catalogue HT (indicatif — à ajuster selon contrat fournisseur) */
  prixAchatHT: number;
  /** Prix de vente conseillé TTC */
  prixVenteTTC: number;
  /** TVA applicable */
  tauxTVA: 5.5 | 20;
  /** Sphere min / max commercialisée */
  sphereMin?: number;
  sphereMax?: number;
  /** Hauteur minimale de montage (mm) pour progressifs */
  hauteurMin?: number;
  /** Mots-clés libres pour la recherche */
  tags?: string[];
}

